phrase = "Corrente Piaui"

# Concatenacao
print(phrase + " e maravilhoso. ")

# Para alterar a String podemos usar funcoes:

print(phrase.upper())

# Tambem podemos realizar combincao com as funcoes

print(phrase.upper().isupper())

# Saber a quantidade de caracteres contidos em uma String

print(len(phrase))


# especificar um caractere contido em uma String

print(phrase[13])

# ou uma index de caracter especifico 

print(phrase.index("i"))

# substituir um pedaco da string

print(phrase.replace("Corrente Piaui", "Alto Fogoso"))